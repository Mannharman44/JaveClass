package com.class7;

public class WhileLoop {

	public static void main(String args[]) {
	 
	int i=1;
	
	while(i<=20) {
		System.out.println(i);
		i++;
		
	}
        int y=10;
        while(y<=30) {
        	System.out.print(y+" ");
        	y++;
        }
	
        int a=10;
        while(a>=1) {
        	System.out.println(a);
        	a--;
        	
        }
        
        int z=2;
        while(z<=20) {
        	System.out.println(z);
        	z+=2;
        	
        }
        int q=1;
        
        while(q<=20) {
        	
        	if (q%2==0) {
        		System.out.println(q);
        	}
        	q++;
        }
        
        
        
        
        
        
        
        
        
        
        
	
	}
	
	
	
}
