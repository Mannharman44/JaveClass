public class forloop {
public static void main(String args[]) {
	
	for (int i=0; i<4; i++) {
		System.out.println("Good morning");
		
	}
	
	for(int a=0; a<=50; a+=5) {
		System.out.println(a);
		
	}
	
	int sum=0;
	  for(int b=1; b<=5;b++) {
		 sum=sum+1 ;
		 System.out.println(sum+ "+");
		 
		  
	  }
		
	
	
}
	
	
	
}
